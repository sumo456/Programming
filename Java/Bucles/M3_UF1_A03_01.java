package Programming.Java.Bucles;

class M3_UF1_A03_01 {
    // Bucle for: Este bucle se ejecutará 10 veces.
    // Se inicializa la variable 'i' en 0. Mientras 'i' sea menor que 10, el ciclo se repetirá, incrementando 'i' en 1 en cada iteración.
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++){
            System.out.println(i);
        }  
    }
}