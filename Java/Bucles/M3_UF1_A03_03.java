package Programming.Java.Bucles;

public class M3_UF1_A03_03 {
    public static void main(String[] args) {
        int i = 9;
                // Bucle while que continuará ejecutándose mientras 'i' sea mayor o igual a 0
        while (i >= 0 ){
            System.out.println(i);
            i--;
        }
    }
}
